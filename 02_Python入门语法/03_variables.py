# 定义一个变量，用来记录钱包余额
money = 50

# 打印出这个变量的值
print ("钱包还有：" , money)

# 买了一个冰淇淋，花费10元
money = money - 10
# 打印出花费之后的余额
print ("买冰淇淋花了10元，还剩",money , "元")


# 每隔一个小时，输出一下钱包的余额
print ("现在是下午1点，钱包余额为：" , money , "元")
print ("现在是下午2点，钱包余额为：" , money , "元")
print ("现在是下午3点，钱包余额为：" , money , "元")
print ("现在是下午4点，钱包余额为：" , money , "元")
print ("现在是下午5点，钱包余额为：" , money , "元")